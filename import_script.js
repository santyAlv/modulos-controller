function determineBrand(model) {
    const m = model.toUpperCase();
    if (m.includes('SAMSUNG') || m.match(/^(MODULO )?[AJM]\d+/)) return 'Samsung';
    if (m.includes('MOTOROLA') || m.includes('MOTO') || m.match(/^(MODULO )?[EG]\d+/) || m.includes('ONE') || m.includes('EDGE')) return 'Motorola';
    if (m.includes('XIAOMI') || m.includes('REDMI') || m.includes('POCO')) return 'Xiaomi';
    if (m.includes('IPHONE') || m.match(/^(MODULO )?\d+[GS]/)) return 'Apple';
    if (m.includes('TCL')) return 'TCL';
    if (m.includes('ALCATEL')) return 'Alcatel';
    if (m.includes('ZTE')) return 'ZTE';
    if (m.includes('NOKIA')) return 'Nokia';
    if (m.includes('LG') || m.match(/^(MODULO )?K\d+/)) return 'LG';
    if (m.includes('BLU')) return 'BLU';
    if (m.includes('NUBIA')) return 'Nubia';
    if (m.includes('ITEL')) return 'Itel';
    if (m.includes('HONOR')) return 'Honor';
    if (m.includes('TECNO')) return 'Tecno';
    if (m.includes('INFINIX')) return 'Infinix';
    if (m.includes('REALME')) return 'Realme';
    if (m.includes('VIVO')) return 'Vivo';
    if (m.includes('OPPO')) return 'Oppo';
    if (m.includes('KINGKONG') || m.includes('UMIDIGI')) return 'Otros';
    return 'Otros';
}

async function importModules() {
    const lines = rawData.split('\n');
    let count = 0;

    console.log('Iniciando importación masiva...');

    for (const line of lines) {
        const parts = line.split('\t').map(p => p.trim());
        for (let i = 0; i < parts.length; i += 2) {
            const model = parts[i];
            let priceStr = parts[i + 1];

            if (model && model.length > 2 && priceStr) {
                // Clean price
                priceStr = priceStr.replace(/[^0-9,]/g, '').replace(',', '.');
                const price = parseFloat(priceStr);

                if (!isNaN(price) && price > 0) {
                    const brand = determineBrand(model);
                    // Clean model name
                    let cleanModel = model.replace(/^MODULO /, '').replace(/^DE /, '');
                    // Capitalize first letter of each word
                    cleanModel = cleanModel.toLowerCase().replace(/(^\w|\s\w)/g, m => m.toUpperCase());

                    const module = {
                        id: Date.now().toString(36) + Math.random().toString(36).substr(2),
                        model: cleanModel,
                        brand: brand,
                        price: price,
                        description: 'Importado automáticamente',
                        imageData: null,
                        createdAt: new Date().toISOString()
                    };

                    await insertModule(module);
                    count++;
                    console.log(`Importado: ${cleanModel} (${brand}) - $${price}`);
                }
            }
        }
    }

    alert(`¡Importación completada! Se agregaron ${count} módulos.`);
    location.reload();
}
